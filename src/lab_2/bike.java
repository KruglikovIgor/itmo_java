package lab_2;

public class bike {
    String name;
    String model;
    double engineCapacity;
    double weight;

    public bike() {
    }

    public bike(String name, String model, double engineCapacity, double weight) {
        this.name = name;
        this.model = model;
        this.engineCapacity = engineCapacity;
        this.weight = weight;
    }
}
